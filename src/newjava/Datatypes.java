package newjava;

public class Datatypes {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		short s=897;
		System.out.println("value of short="+s);
		
		int v=567;
		System.out.println("integer value="+v);
		
		byte b=87;
		System.out.println("value of byte="+b);
		
		long l=678854798l;
		System.out.println("value of long="+l);
		
		float f=7.89f;
		System.out.println("float value="+f);
		
		double d=78.98;
		System.out.println("double value="+d);
		
		char c='g';
		System.out.println("char value="+c);
		
		char c1=69;
		System.out.println("char value of c1="+c1);
		
		boolean b1=20>10;
		System.out.println("value of b1="+b1);
		
		String s1="devan";
		System.out.println("value of string s1="+s1);

	}

}
